def sum(x, y):
    """
    >>> sum(1,2)
    3
    """
    return x+y


def subtract(x, y):
    """
    >>> subtract(2, 5)
    -3
    >>> subtract(7, 5)
    2
    """
    return x-y


def multiply(x, y):
    """
    >>> multiply(3,5)
    15
    >>> multiply(-3,5)
    -15
    """
    return x*y


